<script>
import SimpleGallery from "./SimpleGallery.vue";

export default {
  name: "App",
  components: {
    SimpleGallery,
  },
  data() {
    return {
      images: this.generateImageArray(353), // Generate 50 images
      visibleImageCount: 38,
    };
  },
  computed: {
    showLoadMoreButton() {
      return this.visibleImageCount < this.images.length;
    },
    showLoadLessButton() {
      return this.visibleImageCount > 38;
    },
  },
  methods: {
    loadMoreImages() {
      this.visibleImageCount += 15; // Increase by desired number
    },
    loadLessImages() {
      this.visibleImageCount -= 15; // Decrease by 10
    },
    generateImageArray(count) {
      const images = [];
      for (let i = 1; i <= count; i++) {
        images.push({
          largeURL: `/gallery/${i}.jpg`,
          thumbnailURL: `/gallery/${i}.jpg`,
          width: 5000,
          height: 3000,
        });
      }
      return images;
    },
  },
};
</script>
